package AtividadesColaborativas.colaborativa_10;

public class Reptil extends Animal {

    private boolean peconha;
    private String tipoReproducao;
    private String tipoPele;

    public boolean isPeconha() {
        return peconha;
    }

    public void setPeconha(boolean peconha) {
        this.peconha = peconha;
    }

    public String getTipoReproducao() {
        return tipoReproducao;
    }

    public void setTipoReproducao(String tipoReproducao) {
        this.tipoReproducao = tipoReproducao;
    }

    public String getTipoPele() {
        return tipoPele;
    }

    public void setTipoPele(String tipoPele) {
        this.tipoPele = tipoPele;
    }
}
