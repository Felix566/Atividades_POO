public class ConversaoDeUnidadesDeArea {
    public static double metroQuadrados(double metros){
        return metros * 10.76;
    }
    
    public static double pesQuadrados(double peQuadrado){
        return peQuadrado * 929;
    }
    
    public static double milhasQuadradas(double milhaQuadrada){
        return milhaQuadrada * 640;
    }
    
    public static double acre(double acres){
        return acres * 43.560;
    }
}
